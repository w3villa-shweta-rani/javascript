//switch when a variable has multiple values 

const day = 3;

switch (day) {
    case 1:
        console.log("Monday")
        break;
    case 2:
        console.log("tuesday") 
        break; 
    case 3:
        console.log("wednesday")
        break;
    case 4:
        console.log("thursday")  
        break; 
    case 5:
        console.log("Friday") 
        break;   
    case 6:
        console.log("saturday")   
        break;
    case 7:
        console.log("sunday") 
        break;
    default:
        console.log("Invalid day");        
}
